CREATE TABLE products (
    id SERIAL PRIMARY KEY,
    title VARCHAR(255),
    description TEXT,
    count INTEGER DEFAULT 0,
    price MONEY,
    -- Внешний ключ, связывающий товар с категорией
    category_id INTEGER NOT NULL REFERENCES categories(id)
    ON DELETE CASCADE
);

